function filterReq(toc, str) {
  function copy(o) {
    return Object.assign({}, o);
  }

  function recursiveRemove(list, id) {
    return list
      .map((item) => {
        return { ...item };
      })
      .filter((item) => {
        if ('items' in item) {
          item.items = recursiveRemove(item.items, id);
        }
        return item.title !== id;
      });
  }

  let filtered = () => {
    let copied = copy(toc);
    copied.items = recursiveRemove(toc.items, str);
    return copied;
  };

  return filtered();
}

